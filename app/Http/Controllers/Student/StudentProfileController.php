    public function store(StudentProfileRequest $request){
        $student = Student::make(array_merge($request->only(['first_name', 'middle_name', 'last_name', 'blood_group', 'mother_tongue', 'dob', 'address'], ['user_id'=>auth()->id()])));
        $student->user_id = auth()->id();  // @Todo Make sure that user must be a student via a middlewire
        return $student->save() ? redirect()->route('student') : redirect()->back();
    }
